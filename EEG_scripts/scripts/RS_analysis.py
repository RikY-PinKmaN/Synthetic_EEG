def plot_open_vs_closed(matched_data, source, title, save_path):
    """
    PSD comparison of eyes-open vs eyes-closed for one source (real or synth).
    Highlights the expected alpha suppression (open < closed in alpha band).
    """
    x_o = matched_data['open'][source]
    x_c = matched_data['closed'][source]

    fig, axes = plt.subplots(1, 2, figsize=(16, 6))

    # Left: PSD overlay
    ax = axes[0]
    f_o, psd_o, sem_o, _ = compute_psd(x_o)
    f_c, psd_c, sem_c, _ = compute_psd(x_c)

    psd_o_db = 10 * np.log10(psd_o + 1e-15)
    psd_c_db = 10 * np.log10(psd_c + 1e-15)
    mask = f_o <= PSD_FMAX

    ax.fill_between(f_o[mask], psd_o_db[mask] - 0.5, psd_o_db[mask] + 0.5,
                     alpha=0.15, color=OPEN_COLOR)
    ax.plot(f_o[mask], psd_o_db[mask], color=OPEN_COLOR, lw=2,
            label=f'Eyes Open (n={x_o.shape[2]})')

    ax.fill_between(f_c[mask], psd_c_db[mask] - 0.5, psd_c_db[mask] + 0.5,
                     alpha=0.15, color=CLOSE_COLOR)
    ax.plot(f_c[mask], psd_c_db[mask], color=CLOSE_COLOR, lw=2,
            label=f'Eyes Closed (n={x_c.shape[2]})')

    ax.axvspan(ALPHA_BAND[0], ALPHA_BAND[1], alpha=0.08, color='gold',
               label='Alpha Band')
    ax.set_xlabel('Frequency (Hz)', fontsize=12)
    ax.set_ylabel('PSD (dB µV²/Hz)', fontsize=12)
    ax.set_title('PSD: Open vs Closed', fontsize=13)
    ax.legend(fontsize=10)
    ax.grid(True, alpha=0.3)
    ax.set_xlim([0, PSD_FMAX])

    # Right: Alpha power per channel
    ax = axes[1]
    bp_o, sem_o = compute_band_power(x_o, band=ALPHA_BAND)
    bp_c, sem_c = compute_band_power(x_c, band=ALPHA_BAND)
    x_pos = np.arange(N_CHANNELS)
    bar_w = 0.35

    ax.bar(x_pos - bar_w/2, bp_o, bar_w, yerr=sem_o,
           color=OPEN_COLOR, alpha=0.8, capsize=3, label='Open',
           edgecolor='black', linewidth=0.5)
    ax.bar(x_pos + bar_w/2, bp_c, bar_w, yerr=sem_c,
           color=CLOSE_COLOR, alpha=0.8, capsize=3, label='Closed',
           edgecolor='black', linewidth=0.5)
    ax.set_xticks(x_pos)
    ax.set_xticklabels(CH_NAMES[:N_CHANNELS], fontsize=10)
    ax.set_ylabel('Log Alpha Power (µV²)', fontsize=12)
    ax.set_title('Alpha Power per Channel', fontsize=13)
    ax.legend(fontsize=10)
    ax.grid(True, alpha=0.3, axis='y')

    src_label = 'REAL' if source == 'real' else 'SYNTHETIC'
    fig.suptitle(f'{title} — {src_label}', fontsize=16, fontweight='bold')
    fig.tight_layout()
    fig.savefig(save_path, dpi=150, bbox_inches='tight')
    plt.close(fig)
    print(f"    Saved: {save_path}")


