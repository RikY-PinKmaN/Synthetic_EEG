def attenuate_upsample_artifacts(x, gan_fs=GAN_FS, n_layers=N_UPSAMPLE_LAYERS,
                                  strength=ARTIFACT_STRENGTH):
    """
    Attenuate known Conv1DTranspose(k=4, s=2) spectral artifacts.

    The spike is EXCESS energy on top of real spectral content at
    deterministic frequencies: gan_fs / (2^n) for each layer.
    Blending the notched signal with the original removes the excess
    without creating a dip:
        x_out = strength * x_notched + (1 - strength) * x_original

    Applied to synthetic data during comparison only (not during GAN
    training or evaluation) — analogous to powerline noise removal
    in real EEG acquisition pipelines.

    x : (samples, channels, trials) — denormalized synthetic data in µV
    """
    if strength <= 0.0:
        return x

    nyq = gan_fs / 2.0
    artifact_freqs = []
    for layer in range(1, n_layers):
        f_art = gan_fs / (2 ** (n_layers - layer + 1))
        if 1.0 < f_art < nyq - 1.0:
            artifact_freqs.append(f_art)

    if not artifact_freqs:
        return x

    x_clean = np.copy(x)
    for f_notch in artifact_freqs:
        b, a = iirnotch(f_notch, Q=30, fs=gan_fs)
        for t in range(x_clean.shape[2]):        # trials
            for c in range(x_clean.shape[1]):    # channels
                notched = filtfilt(b, a, x_clean[:, c, t])
                x_clean[:, c, t] = strength * notched + (1 - strength) * x_clean[:, c, t]

    return x_clean

def _find_marker_column(raw_array, marker_val, n_eeg_ch):
    """Find the column containing the start marker (scan from right)."""
    n_cols = raw_array.shape[1]
    for col in range(n_cols - 1, max(n_cols - 6, n_eeg_ch), -1):
        if marker_val in raw_array[:, col]:
            return col
    for col in range(n_eeg_ch, n_cols):
        if marker_val in raw_array[:, col]:
            return col
    return -1


def load_and_filter_easy(filepath):
    """
    Load a single .easy file and apply the same preprocessing as nbase.py:
      1. Load text file
      2. Find marker column, slice from start marker
      3. Unit conversion (nV -> µV if needed)
      4. Powerline notch (50 Hz + 100 Hz harmonic)
      5. Butterworth bandpass (2-40 Hz, order 4)

    Returns: (n_samples, n_channels) at NATIVE_FS, or None on failure.
    """
    try:
        raw = np.loadtxt(filepath)
    except Exception as e:
        print(f"    Cannot read {filepath}: {e}")
        return None

    if raw.shape[1] <= N_CHANNELS:
        return None

    mc = _find_marker_column(raw, MARKER_VAL, N_CHANNELS)
    if mc == -1:
        return None

    starts = np.where(raw[:, mc] == MARKER_VAL)[0]
    if len(starts) == 0:
        return None
    si = starts[0]

    n_sam = int(NATIVE_FS * DURATION_S)
    eeg = raw[si:si + n_sam, :N_CHANNELS] if si + n_sam <= len(raw) else raw[si:, :N_CHANNELS]

    if eeg.shape[0] < int(0.5 * n_sam):
        return None

    # Unit conversion
    if np.median(np.abs(eeg)) > 500:
        eeg = eeg / 1000.0

    eeg = eeg.astype(np.float64)

    # Powerline notch (50 Hz + harmonic at 100 Hz)
    if POWERLINE_HZ > 0:
        for freq in [POWERLINE_HZ, POWERLINE_HZ * 2]:
            if freq < NATIVE_FS / 2:
                b_n, a_n = iirnotch(freq, Q=30, fs=NATIVE_FS)
                eeg = filtfilt(b_n, a_n, eeg, axis=0)

    # Bandpass
    nyq = 0.5 * NATIVE_FS
    lo = max(1e-6, min(LOWCUT / nyq, 1 - 1e-6))
    hi = max(1e-6, min(HIGHCUT / nyq, 1 - 1e-6))
    b, a = butter(4, [lo, hi], btype='band')
    filtered = filtfilt(b, a, eeg, axis=0)

    return filtered


def epoch_and_reject(continuous, label):
    """
    Epoch continuous data into fixed-length segments with artifact rejection.
    Returns: x (samples, channels, n_epochs), y (n_epochs,)
    """
    n_samples = continuous.shape[0]
    epoch_len = int(EPOCH_S * NATIVE_FS)
    stride    = int(STRIDE_S * NATIVE_FS)

    epochs = []
    for start in range(0, n_samples - epoch_len + 1, stride):
        ep = continuous[start:start + epoch_len, :]    # (epoch_len, n_ch)
        # Artifact rejection: peak-to-peak per channel
        ptp = np.max(ep, axis=0) - np.min(ep, axis=0)
        if np.any(ptp > ARTIFACT_THRESH):
            continue
        epochs.append(ep)

    if not epochs:
        return np.empty((epoch_len, N_CHANNELS, 0)), np.array([])

    x = np.array(epochs).transpose(1, 2, 0)  # (samples, channels, n_epochs)
    y = np.full(x.shape[2], label)
    return x, y


def decimate_trials(x, factor):
    """
    Anti-aliased decimation along time axis (axis=0).
    x: (samples, channels, trials) -> (samples//factor, channels, trials)
    """
    S, C, T = x.shape
    out_S = S // factor
    out = np.zeros((out_S, C, T), dtype=x.dtype)
    for t in range(T):
        for c in range(C):
            dec = decimate(x[:, c, t].astype(np.float64), factor,
                           ftype='fir', zero_phase=True)
            out[:, c, t] = dec[:out_S]
    return out


def load_real_group(session, group):
    """
    Load all real data for one group (Young or Elderly), one session (pre or post).

    Returns dict:
      {
        'open':   (samples_dec, channels, total_open_epochs)  in µV at GAN_FS,
        'closed': (samples_dec, channels, total_closed_epochs) in µV at GAN_FS,
        'n_subjects': int
      }
    """
    op_pat = 'rs1o' if session == 'pre' else 'rs2o'
    cl_pat = 'rs1c' if session == 'pre' else 'rs2c'

    gdir = os.path.join(RS_DATA_ROOT, group)
    if not os.path.isdir(gdir):
        print(f"  WARNING: directory not found: {gdir}")
        return None

    sub_dirs = sorted(d for d in glob.glob(os.path.join(gdir, '*'))
                      if os.path.isdir(d))

    all_open, all_closed = [], []
    n_subj = 0

    for sd in sub_dirs:
        files = glob.glob(os.path.join(sd, '*.easy'))
        subj_open, subj_closed = [], []

        for fp in files:
            fn = os.path.basename(fp).lower()
            if op_pat in fn:
                cond = 'open'
            elif cl_pat in fn:
                cond = 'closed'
            else:
                continue

            continuous = load_and_filter_easy(fp)
            if continuous is None:
                continue

            lv = LABEL_OPEN if cond == 'open' else LABEL_CLOSED
            x, y = epoch_and_reject(continuous, lv)
            if x.shape[2] == 0:
                continue

            # Decimate to GAN_FS
            x_dec = decimate_trials(x, DECIMATE_FACTOR)

            if cond == 'open':
                subj_open.append(x_dec)
            else:
                subj_closed.append(x_dec)

        if subj_open and subj_closed:
            n_subj += 1
            all_open.extend(subj_open)
            all_closed.extend(subj_closed)

    if not all_open or not all_closed:
        return None

    x_open   = np.concatenate(all_open, axis=2)
    x_closed = np.concatenate(all_closed, axis=2)

    print(f"  Real [{group}/{session}]: {n_subj} subjects, "
          f"open={x_open.shape[2]} epochs, closed={x_closed.shape[2]} epochs")

    return {'open': x_open, 'closed': x_closed, 'n_subjects': n_subj}


