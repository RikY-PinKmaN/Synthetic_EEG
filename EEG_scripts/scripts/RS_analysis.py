def plot_summary_dashboard(all_data, save_path):
    """
    Single-page dashboard summarising alpha power across all conditions.
    Rows: Young, Elderly.  Cols: Pre Open, Pre Closed, Post Open, Post Closed.
    Each cell: Real vs Synthetic bar for one posterior channel (Oz or last channel).
    """
    # Use posterior channel (last one, typically Oz)
    ch_idx = N_CHANNELS - 1
    ch_name = CH_NAMES[ch_idx] if ch_idx < len(CH_NAMES) else f'Ch{ch_idx}'

    fig, axes = plt.subplots(2, 4, figsize=(20, 8), sharey=True)

    groups = ['Young', 'Elderly']
    for gi, group in enumerate(groups):
        for ci, (cond, state) in enumerate(
            [('pre', 'open'), ('pre', 'closed'),
             ('post', 'open'), ('post', 'closed')]):

            ax = axes[gi][ci]
            key = (group, cond)
            if key not in all_data or all_data[key] is None:
                ax.text(0.5, 0.5, 'No data', ha='center', va='center',
                        transform=ax.transAxes, fontsize=12)
                continue

            matched = all_data[key]
            x_r = matched[state]['real']
            x_s = matched[state]['synth']

            # Alpha power at the selected channel
            bp_r, _ = compute_band_power(x_r, band=ALPHA_BAND)
            bp_s, _ = compute_band_power(x_s, band=ALPHA_BAND)

            bars = ax.bar(['Real', 'Synth'], [bp_r[ch_idx], bp_s[ch_idx]],
                          color=[REAL_COLOR, SYNTH_COLOR], alpha=0.8,
                          edgecolor='black', linewidth=0.5)

            # Add value labels
            for bar, val in zip(bars, [bp_r[ch_idx], bp_s[ch_idx]]):
                ax.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 0.05,
                        f'{val:.2f}', ha='center', fontsize=9)

            st_label = 'Open' if state == 'open' else 'Closed'
            ax.set_title(f'{cond.capitalize()} — {st_label}', fontsize=11)
            ax.grid(True, alpha=0.3, axis='y')

        axes[gi][0].set_ylabel(f'{group}\nLog Alpha ({ch_name})', fontsize=11)

    fig.suptitle('Summary: Alpha Power at Posterior Channel — Real vs Synthetic',
                 fontsize=16, fontweight='bold')
    fig.tight_layout()
    fig.savefig(save_path, dpi=150, bbox_inches='tight')
    plt.close(fig)
    print(f"    Saved: {save_path}")


