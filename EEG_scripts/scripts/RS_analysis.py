def load_synthetic_group(session, subject_ids):
    """
    Load and denormalize synthetic data for a set of subject IDs.

    Each subject's .mat file contains:
      synthetic_x : (trials, channels, T) in [-1, 1]
      synthetic_y : (trials,) class labels
      norm_method : 'minmax' (for RS)
      norm_global_min, norm_global_max : scalar denorm params

    Returns dict:
      {
        'open':   (samples, channels, total_open_epochs) in µV at GAN_FS,
        'closed': (samples, channels, total_closed_epochs) in µV at GAN_FS,
        'n_subjects': int
      }
    """
    all_open, all_closed = [], []
    n_subj = 0

    for sid in subject_ids:
        mat_path = os.path.join(RESULTS_ROOT, session,
                                f'Subject_{sid}', f'synthetic_S{sid}.mat')
        if not os.path.exists(mat_path):
            print(f"    Synthetic not found: {mat_path}")
            continue

        mat = loadmat(mat_path)
        x_norm = mat['synthetic_x']         # (trials, channels, T) in [-1,1]
        y      = mat['synthetic_y'].ravel()  # (trials,)
        method = str(mat['norm_method'][0]) if 'norm_method' in mat else 'minmax'

        # Denormalize to µV
        if method == 'minmax':
            g_min = float(mat['norm_global_min'])
            g_max = float(mat['norm_global_max'])
            g_range = g_max - g_min
            x_uv = (x_norm + 1.0) / 2.0 * g_range + g_min
        else:
            ch_mean = mat['norm_ch_mean']
            ch_std  = mat['norm_ch_std']
            clip_sigma = float(mat.get('norm_clip_sigma', 3.0))
            x_uv = x_norm * clip_sigma * ch_std + ch_mean

        # x_uv is (trials, channels, T).
        # Convert to (samples, channels, trials) for consistency with real data.
        x_uv = x_uv.transpose(2, 1, 0)   # (T, channels, trials)

        mask_open   = (y == LABEL_OPEN)
        mask_closed = (y == LABEL_CLOSED)

        if mask_open.any():
            all_open.append(x_uv[:, :, mask_open])
        if mask_closed.any():
            all_closed.append(x_uv[:, :, mask_closed])
        n_subj += 1

    if not all_open or not all_closed:
        return None

    x_open   = np.concatenate(all_open, axis=2)
    x_closed = np.concatenate(all_closed, axis=2)

    # Attenuate Conv1DTranspose upsampling artifacts (partial notch)
    if ARTIFACT_STRENGTH > 0:
        x_open   = attenuate_upsample_artifacts(x_open)
        x_closed = attenuate_upsample_artifacts(x_closed)

    print(f"  Synth [{session}]: {n_subj} subjects, "
          f"open={x_open.shape[2]} epochs, closed={x_closed.shape[2]} epochs"
          + (f"  (artifact atten={ARTIFACT_STRENGTH})" if ARTIFACT_STRENGTH > 0 else ""))

    return {'open': x_open, 'closed': x_closed, 'n_subjects': n_subj}


def match_epoch_counts(real_data, synth_data):
    """
    Subsample the larger set (real or synthetic) to match the smaller set.
    This ensures fair comparison with equal trial counts.
    Returns copies with matched sizes.
    """
    out = {}
    for state in ['open', 'closed']:
        nr = real_data[state].shape[2]
        ns = synth_data[state].shape[2]
        n = min(nr, ns)
        # Random subsample from whichever is larger
        rng = np.random.default_rng(42)
        r_idx = rng.choice(nr, n, replace=False) if nr > n else np.arange(nr)
        s_idx = rng.choice(ns, n, replace=False) if ns > n else np.arange(ns)
        out[state] = {
            'real':  real_data[state][:, :, r_idx],
            'synth': synth_data[state][:, :, s_idx],
            'n': n,
        }
    return out


def apply_global_power_correction(matched_data):
    """
    Apply a single global RMS amplitude correction to synthetic data to
    compensate for systematic tanh activation range compression.

    One scalar computed from ALL real and synthetic data pooled together.
    No per-channel, per-frequency, or per-condition fitting.
    """
    real_all  = np.concatenate([matched_data[s]['real']  for s in ['open', 'closed']], axis=2)
    synth_all = np.concatenate([matched_data[s]['synth'] for s in ['open', 'closed']], axis=2)

    rms_real  = np.sqrt(np.mean(real_all ** 2))
    rms_synth = np.sqrt(np.mean(synth_all ** 2))

    if rms_synth < 1e-10:
        return matched_data, 1.0

    gain = rms_real / rms_synth
    for state in ['open', 'closed']:
        matched_data[state]['synth'] = matched_data[state]['synth'] * gain

    return matched_data, gain


