def compute_psd(x, fs=GAN_FS, nperseg=PSD_NPERSEG):
    """
    Compute average PSD across all trials and channels.
    x: (samples, channels, trials)
    Returns: freqs, psd_mean (averaged over trials), psd_per_ch (channels,freqs)
    """
    S, C, T = x.shape
    all_psd = []
    for t in range(T):
        ch_psd = []
        for c in range(C):
            f, p = welch(x[:, c, t], fs=fs, nperseg=min(nperseg, S))
            ch_psd.append(p)
        all_psd.append(ch_psd)

    # (trials, channels, freq_bins)
    all_psd = np.array(all_psd)
    psd_per_ch = np.mean(all_psd, axis=0)    # (channels, freq_bins)
    psd_mean   = np.mean(psd_per_ch, axis=0)  # (freq_bins,)
    psd_sem    = np.std(np.mean(all_psd, axis=1), axis=0) / np.sqrt(T)

    return f, psd_mean, psd_sem, psd_per_ch


def compute_band_power(x, fs=GAN_FS, band=ALPHA_BAND, nperseg=PSD_NPERSEG):
    """
    Compute average band power per channel across all trials.
    x: (samples, channels, trials)
    Returns: (channels,) array of mean log band power.
    """
    S, C, T = x.shape
    bp = np.zeros((C, T))
    for t in range(T):
        for c in range(C):
            f, p = welch(x[:, c, t], fs=fs, nperseg=min(nperseg, S))
            mask = (f >= band[0]) & (f <= band[1])
            bp[c, t] = np.log(np.mean(p[mask]) + 1e-10) if mask.any() else 0.
    return np.mean(bp, axis=1), np.std(bp, axis=1) / np.sqrt(T)


def compute_grand_average(x):
    """
    Grand average waveform across all trials, per channel.
    x: (samples, channels, trials)
    Returns: mean (samples, channels), sem (samples, channels)
    """
    mean = np.mean(x, axis=2)
    sem  = np.std(x, axis=2) / np.sqrt(x.shape[2])
    return mean, sem


