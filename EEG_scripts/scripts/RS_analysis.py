def plot_psd_comparison(matched_data, title, save_path):
    """
    Side-by-side PSD for eyes-open and eyes-closed.
    Shows full spectrum with alpha band shaded.
    """
    fig, axes = plt.subplots(1, 2, figsize=(16, 6), sharey=True)

    for ax, state, state_label in zip(axes, ['open', 'closed'],
                                       ['Eyes Open', 'Eyes Closed']):
        x_r = matched_data[state]['real']
        x_s = matched_data[state]['synth']

        f_r, psd_r, sem_r, _ = compute_psd(x_r)
        f_s, psd_s, sem_s, _ = compute_psd(x_s)

        # Plot in dB
        psd_r_db = 10 * np.log10(psd_r + 1e-15)
        psd_s_db = 10 * np.log10(psd_s + 1e-15)
        sem_r_db = 10 * sem_r / (psd_r * np.log(10) + 1e-15)
        sem_s_db = 10 * sem_s / (psd_s * np.log(10) + 1e-15)

        mask = f_r <= PSD_FMAX
        ax.fill_between(f_r[mask], psd_r_db[mask] - sem_r_db[mask],
                         psd_r_db[mask] + sem_r_db[mask],
                         alpha=0.2, color=REAL_COLOR)
        ax.plot(f_r[mask], psd_r_db[mask], color=REAL_COLOR, lw=2,
                label=f'Real (n={x_r.shape[2]})')

        ax.fill_between(f_s[mask], psd_s_db[mask] - sem_s_db[mask],
                         psd_s_db[mask] + sem_s_db[mask],
                         alpha=0.2, color=SYNTH_COLOR)
        ax.plot(f_s[mask], psd_s_db[mask], color=SYNTH_COLOR, lw=2,
                label=f'Synthetic (n={x_s.shape[2]})')

        # Shade alpha band
        ax.axvspan(ALPHA_BAND[0], ALPHA_BAND[1], alpha=0.08, color='gold',
                   label=f'Alpha ({ALPHA_BAND[0]}-{ALPHA_BAND[1]} Hz)')

        ax.set_xlabel('Frequency (Hz)', fontsize=12)
        ax.set_title(state_label, fontsize=14)
        ax.legend(fontsize=10)
        ax.grid(True, alpha=0.3)
        ax.set_xlim([0, PSD_FMAX])

    axes[0].set_ylabel('PSD (dB µV²/Hz)', fontsize=12)
    fig.suptitle(title, fontsize=16, fontweight='bold')
    fig.tight_layout()
    fig.savefig(save_path, dpi=150, bbox_inches='tight')
    plt.close(fig)
    print(f"    Saved: {save_path}")


def plot_alpha_topomap(matched_data, title, save_path):
    """
    Topoplot of alpha band power for real vs synthetic, open vs closed.
    Uses MNE's built-in topoplot functionality natively. 
    Falls back to a standard bar chart if MNE is not available.
    """
    panels = [
        (0, 0, 'open',   'real',  'Real — Eyes Open'),
        (0, 1, 'open',   'synth', 'Synthetic — Eyes Open'),
        (1, 0, 'closed', 'real',  'Real — Eyes Closed'),
        (1, 1, 'closed', 'synth', 'Synthetic — Eyes Closed'),
    ]

    # Compute all alpha powers first for consistent colour scale across all 4 subplots
    bp_dict = {}
    all_bp = []
    for r, c, state, source, label in panels:
        x = matched_data[state][source]
        bp_mean, bp_sem = compute_band_power(x, band=ALPHA_BAND)
        bp_dict[(state, source)] = (bp_mean, bp_sem)
        all_bp.append(bp_mean)

    all_bp = np.array(all_bp)
    vmin, vmax = all_bp.min(), all_bp.max()

    if HAS_MNE:
        # Create MNE info with standard 10-20 positions
        info = mne.create_info(ch_names=CH_NAMES[:N_CHANNELS],
                               sfreq=GAN_FS, ch_types='eeg')
        montage = mne.channels.make_standard_montage('standard_1020')
        info.set_montage(montage, on_missing='warn')

        fig, axes = plt.subplots(2, 2, figsize=(10, 8))
        
        # FIX: 
        # 1. extrapolate='head' fills the whole head circle smoothly (removes the hexagon).
        # 2. sphere=(0.0, 0.0, 0.0, 0.095) locks the scaling to the standard head size, 
        #    keeping Fp1, Fp2, and Oz properly inside the head outline.
        kwargs_topo = dict(
            show=False, 
            cmap='RdYlBu_r', 
            vlim=(vmin, vmax), 
            extrapolate='head', 
            sphere=(0.0, 0.0, 0.0, 0.095)
        )

